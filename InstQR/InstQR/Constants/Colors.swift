import SwiftUI

extension Color {
    public static var ButtonBackground: Color {
        return Color(cgColor: CGColor(red: 0.0, green: 0.2, blue: 0.72, alpha: 1))
    }
    
    public static var InputBackground: Color {
        return Color(cgColor: CGColor(red: 0, green: 0, blue: 0, alpha: 0))
    }
    
    public static var PlaceholderText: Color {
        return Color(cgColor: CGColor(red: 0.35, green: 0.35, blue: 0.35, alpha: 1))
    }

}
